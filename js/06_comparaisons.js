// L'opérateur de comparaison == : comparaison de valleur (égal à )
var nb1 = 123;
var nb2 = "123";
console.log(nb1 == nb2);

// L'opérateur de comparaison === : comparaison valeur et type (identique)
console.log(nb1 === nb2);

// L'opérateur de comparaison != : comparaison de valleur (différent)
console.log(nb1 != nb2);

// L'opérateur de comparaison !== : comparaison valeur et type (strictement différent)
console.log(nb1 !== nb2);





